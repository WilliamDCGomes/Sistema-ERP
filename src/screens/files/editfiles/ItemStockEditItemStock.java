    private void buttonEditItemAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditItemAddItemActionPerformed
        if(buttonEditItemAddItem.getText().equals("SALVAR")){
            JOptionPane.showMessageDialog(null, "ITEM CADASTRADO COM SUCESSO");
            CallItemScreen callOrderScreen = new CallItemScreen();
            this.dispose();
            callOrderScreen.callScreen();
        }
        else{
            CallItemEdit callOrderEdit = new CallItemEdit();
            this.dispose();
            callOrderEdit.callScreen();
        }
    }//GEN-LAST:event_buttonEditItemAddItemActionPerformed

